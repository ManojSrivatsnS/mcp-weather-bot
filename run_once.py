def run_scheduler_once():
    print("â° Running weather mailer scheduler...")
    users = load_users()
    today_str = datetime.utcnow().strftime("%Y-%m-%d")
    print(f"ðŸ“‹ Loaded {len(users)} users from sheet.")

    for user in users:
        email = user.get("email")
        city = user.get("city")
        location = user.get("location")
        timezone = user.get("timezone")
        last_sent = user.get("last_sent_date", "")

        if not all([email, location, timezone]):
            print(f"âš ï¸ Skipping incomplete user: {user}")
            continue

        try:
            if is_within_window(timezone):
                if last_sent != today_str:
                    print(f"ðŸ“© Sending weather to {email} ({city})")
                    weather = get_weather(location)
                    send_email(email, weather, city)
                    update_last_sent_date(email)
                else:
                    print(f"âœ… Already sent to {email} today, skipping.")
            else:
                print(f"â³ Skipping {email} â€” not near 7AM window in {timezone}")
        except Exception as e:
            print(f"âŒ Error processing {email}: {e}")

if __name__ == "__main__":
    run_scheduler_once()
